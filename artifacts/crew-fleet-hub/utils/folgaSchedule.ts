function pad(n: number): string {
  return String(n).padStart(2, "0");
}

export function getFolgaDays(group: FolgaGroup, year: number, month: number): string[] {
  let days: number[] = [];
  if (year === 2026 && month >= 0 && month <= 11) {
    days = SCHEDULE_2026[month][group] ?? [];
  } else if (year === 2027 && month === 0) {
    days = SCHEDULE_2027_JAN[group] ?? [];
  }
  return days.map((d) => `${year}-${pad(month + 1)}-${pad(d)}`);
}

export function isFolgaDay(group: FolgaGroup, dateIso: string): boolean {
  const [y, m] = dateIso.split("-").map(Number);
  const days = getFolgaDays(group, y, m - 1);
  return days.includes(dateIso);
}

export function getFolgaDaysForYear(group: FolgaGroup, year: number): string[] {
  const result: string[] = [];
  for (let m = 0; m < 12; m++) {
    result.push(...getFolgaDays(group, year, m));
  }
  return result;
}
